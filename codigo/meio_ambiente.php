            <h2>Nosso propósito é o que faz a diferença</h2>

            <p>Com o propósito de minimizar o problema do lixo eletrônico a <strong>FAVAI-Tech</strong> se preocupa com: a preservação do meio ambiente e com políticas educacionais que enfocam a não geração, o consumo sustentável (redução); a reutilização de produtos eletrônicos; a reciclagem com o retorno para a cadeia produtiva e finalmente a correta disposição dos resíduos. Esta preocupação é evidente, pois o lixo eletrônico, em sua maioria, contem mais de 60 elementos, muitos deles tóxicos e valiosos que podem voltar para o processo produtivo reduzindo o consumo dos mesmos.</p>

            <p>Uma forma de reduzir o impacto ao meio ambiente é criar um fluxo econômico fechado, pois os nossos recursos naturais estão se perdendo devido à baixa eficiência na sua extração, tecnologias de reciclagem pouco eficientes e alta contaminação ligada ao processo de reciclagem.</p>

            <p>Os equipamentos eletrônicos são feitos, em sua maioria de metais e plásticos. Um exemplo é o telefone celular: ele pode conter mais de 40 elementos, como o cobre, estanho, cobalto, índio, antimônio, prata, paládio e ouro. Em média utiliza-se 250 mg de prata, 24 mg de ouro, 9 mg de paládio e 9 g de cobre, sem contar a bateria, que contem 3,5 g de cobalto. Segundo a ONU, se tomarmos como base a venda mundial de 1,2 bilhões de aparelhos celulares em 2007 e 255 milhões de computadores portáteis, temos aproximadamente 555 toneladas de Prata, 85 toneladas de Ouro, 31 toneladas de Paládio, 139.000 toneladas de Cobre e 11.000 toneladas de Cobalto.</p>

            <p>Outros metais também são usados, como por exemplo, o ferro. É utilizado nos computadores de mesa, com uma média de 6 quilos de ferro para um único desktop, contabilizando 930.000.000 quilos de ferro para a produção de todos os desktops produzidos em 2007.</p>

            <p>Fica evidente que ao reciclar esses metais dos componentes eletroeletrônicos, utilizaremos menos recursos naturais. Outro ponto importante que devemos saber é que para extrair os metais da natureza utilizamos muita ENERGIA, mas ao utilizarmos o metal da reciclagem não precisaremos mais gastar a energia da etapa da extração e somente pós extração. Ao não precisarmos consumir essa energia para a extração, estamos também diminuindo as emissões de CO2, que é um dos causadores do aquecimento global.</p>

            <p>Um exemplo de reciclagem eficiente é o alumínio, que ao ser reciclado economiza mais de 90% de energia caso tivesse que ser extraído da natureza para ser produzido. Isso faz com que haja a redução de duas vezes o seu peso reciclado na emissão de CO2, não gerando 1,3 vezes o seu peso reciclado de resíduo de bauxita e 0,011 vezes seu peso em SO2.</p>

